inline uint8_t xTime(uint8_t x){
  return ((x<<1) ^ (((x>>7) & 1) * 0x1b));
}

#define Multiply(x, y)                                \
      (  ((y & 1) * x) ^                              \
      ((y>>1 & 1) * xTime(x)) ^                       \
      ((y>>2 & 1) * xTime(xTime(x))) ^                \
      ((y>>3 & 1) * xTime(xTime(xTime(x)))) ^         \
      ((y>>4 & 1) * xTime(xTime(xTime(xTime(x))))))   \


